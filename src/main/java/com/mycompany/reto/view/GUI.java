    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            List<Consulta1Vo> proyectos = controller.listaTotalLider();
            String salida = "Reporte 1";
            for(Consulta1Vo proyecto: proyectos){
                    salida+="\n";
                    salida+=proyecto.getId();
                    salida+="\t";
                    salida+=proyecto.getNombre();
                    salida+="\t";
                    salida+=proyecto.getApellido();
                    salida+="\t";
                    salida+=proyecto.getCiudad();
                    salida+="\t";
            }
            jTextArea2.setText(salida);
        } catch (SQLException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

